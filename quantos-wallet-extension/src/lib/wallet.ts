/// keccak256("claim()") first 4 bytes
const QTEST_CLAIM_SELECTOR = '4e71d92d'

/**
 * Claim 1 QTEST token from the faucet contract.
 * The contract enforces a 24h cooldown per address.
 */
export async function claimFaucet(
  wallet: UnlockedWallet,
  qtestContractAddress: string,
  rpcUrl?: string,
): Promise<SendResult> {
  return callContract(
    wallet,
    {
      contractAddress: qtestContractAddress,
      calldataHex: QTEST_CLAIM_SELECTOR,
      amount: '0',
    },
    rpcUrl,
  )
}

// ── Message Signing ─────────────────────────────────────────

export interface SignedMessage {
  message: string
  signature: string
  publicKey: string
}

/**
 * Sign an arbitrary message (hex or utf8) with the wallet.
 */
export async function signMessage(
  wallet: UnlockedWallet,
  message: string,
  encoding: 'hex' | 'utf8' = 'utf8',
): Promise<SignedMessage> {
  const wasm = await getWasm()
  const messageHex = encoding === 'hex' ? message : bytesToHex(new TextEncoder().encode(message))
  const signatureHex = wasm.signMessage(messageHex, wallet.secretKey)

  return {
    message,
    signature: signatureHex,
    publicKey: wallet.publicKey,
  }
}

/**
 * Verify a message signature.
 */
export async function verifySignature(
  message: string,
  signature: string,
  publicKey: string,
  encoding: 'hex' | 'utf8' = 'utf8',
): Promise<boolean> {
  const wasm = await getWasm()
  const messageHex = encoding === 'hex' ? message : bytesToHex(new TextEncoder().encode(message))
  return wasm.verifySignature(signature, messageHex, publicKey)
}

// ── Helpers ─────────────────────────────────────────────────

function hexToBytes(hex: string): Uint8Array {
  const bytes = new Uint8Array(hex.length / 2)
  for (let i = 0; i < hex.length; i += 2) {
    bytes[i / 2] = parseInt(hex.substring(i, i + 2), 16)
  }
  return bytes
}

function bytesToHex(bytes: Uint8Array): string {
  return Array.from(bytes)
    .map((b) => b.toString(16).padStart(2, '0'))
    .join('')
}
