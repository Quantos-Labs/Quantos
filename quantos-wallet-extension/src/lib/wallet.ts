// ── Balance & Account ───────────────────────────────────────

export interface BalanceInfo {
  balance: string       // decimal string
  formatted: string     // human readable (e.g. "1.234567 QTS")
  stake: string         // decimal string
  isValidator: boolean
}

/**
 * Get wallet balance with human readable formatting.
 */
export async function getBalance(
  wallet: UnlockedWallet,
  rpcUrl?: string,
): Promise<BalanceInfo> {
  if (rpcUrl) {
    rpc.setRpcUrl(rpcUrl)
  }

  const [balanceRaw, account] = await Promise.all([
    rpc.getBalance(wallet.rpcAddress),
    rpc.getAccount(wallet.rpcAddress),
  ])

  const balance = rpc.parseQtsHex(balanceRaw).toString()
  const stake = rpc.parseQtsHex(account.stake).toString()

  // Format with 6 decimal places (adjust as needed)
  const formatted = formatQtest(balance)

  return {
    balance,
    formatted,
    stake,
    isValidator: account.is_validator,
  }
}

/**
 * Format QTEST amount from smallest unit to human readable.
 */
export function formatQtest(amount: string, decimals: number = 6): string {
  const value = BigInt(amount)
  const divisor = 10n ** BigInt(decimals)
  const whole = value / divisor
  const fraction = value % divisor
  return `${whole}.${fraction.toString().padStart(decimals, '0')} QTEST`
}

/**
 * Parse human readable QTEST amount to smallest unit.
 */
export function parseQtest(formatted: string): string {
  const [whole, fraction = '0'] = formatted.replace(' QTEST', '').replace(' QTS', '').split('.')
  const decimals = fraction.length
  const value = BigInt(whole) * 10n ** BigInt(decimals) + BigInt(fraction)
  return value.toString()
}

