// ── Quantos Wallet Manager ──
// Manages wallet lifecycle: create, import, unlock, lock, sign transactions.
// Wallets are stored encrypted with PIN in chrome.storage.local (extension)
// or localStorage (web app).

import { encryptWithPin, decryptWithPin } from './crypto'
import * as rpc from './rpc'

// ── Types ───────────────────────────────────────────────────

export interface StoredWallet {
  address: string           // hex-encoded 32-byte address
  qtsAddress: string        // qts1... format
  rpcAddress: string        // QTS:<hex> for RPC calls
  publicKey: string         // hex-encoded Dilithium public key
  encryptedSecretKey: string // AES-256-GCM encrypted secret key (base64)
  createdAt: number
}

export interface UnlockedWallet {
  address: string
  qtsAddress: string
  rpcAddress: string        // QTS:<hex> for RPC calls
  publicKey: string
  secretKey: string         // hex-encoded (in memory only, never persisted)
}

export interface WalletStore {
  wallets: StoredWallet[]
  activeAddress: string | null
}

// ── Storage Abstraction ─────────────────────────────────────
// Works with both chrome.storage.local (extension) and localStorage (web)

const STORAGE_KEY = 'quantos_wallets'

function isExtension(): boolean {
  return typeof chrome !== 'undefined' && !!chrome?.storage?.local
}

async function loadStore(): Promise<WalletStore> {
  if (isExtension()) {
    return new Promise((resolve) => {
      chrome.storage.local.get([STORAGE_KEY], (result) => {
        resolve(result[STORAGE_KEY] || { wallets: [], activeAddress: null })
      })
    })
  }
  const raw = localStorage.getItem(STORAGE_KEY)
  return raw ? JSON.parse(raw) : { wallets: [], activeAddress: null }
}

async function saveStore(store: WalletStore): Promise<void> {
  if (isExtension()) {
    return new Promise((resolve) => {
      chrome.storage.local.set({ [STORAGE_KEY]: store }, resolve)
    })
  }
  localStorage.setItem(STORAGE_KEY, JSON.stringify(store))
}

// ── WASM Module ─────────────────────────────────────────────
// Dynamic import of the WASM module (loaded once)

let wasmModule: typeof import('../../pkg/quantos_wallet_core') | null = null

async function getWasm() {
  if (!wasmModule) {
    // @ts-ignore - WASM module is generated by wasm-pack
    const mod = await import('../../pkg/quantos_wallet_core')
    await mod.default() // Initialize WASM
    wasmModule = mod
  }
  return wasmModule
}

// ── Wallet Operations ───────────────────────────────────────

/**
 * Create a new wallet encrypted with PIN.
 * Returns the stored wallet (no secret key exposed).
 */
export async function createWallet(pin: string): Promise<StoredWallet> {
  if (pin.length !== 6 || !/^\d{6}$/.test(pin)) {
    throw new Error('PIN must be exactly 6 digits')
  }

  const wasm = await getWasm()

  // Generate Dilithium-3 keypair
  const keypairJson = wasm.generateKeypair()
  const keypair = JSON.parse(keypairJson) as {
    publicKey: string
    secretKey: string
    address: string
    qtsAddress: string
  }

  // Encrypt secret key with PIN
  const encryptedSecretKey = await encryptWithPin(keypair.secretKey, pin)

  const wallet: StoredWallet = {
    address: keypair.address,
    qtsAddress: keypair.qtsAddress,
    rpcAddress: `QTS:${keypair.address}`,
    publicKey: keypair.publicKey,
    encryptedSecretKey,
    createdAt: Date.now(),
  }

  // Save to storage
  const store = await loadStore()
  store.wallets.push(wallet)
  if (!store.activeAddress) {
    store.activeAddress = wallet.address
  }
  await saveStore(store)

  return wallet
}

/**
 * Import a wallet from a secret key (hex) + PIN.
 * The public key and address are derived from the secret key.
 * Dilithium-3 stores the public key in the last 1952 bytes of the secret key.
 */
export async function importWallet(secretKeyHex: string, pin: string): Promise<StoredWallet> {
  if (pin.length !== 6 || !/^\d{6}$/.test(pin)) {
    throw new Error('PIN must be exactly 6 digits')
  }

  // Validate secret key format (4000 bytes = 8000 hex chars)
  if (secretKeyHex.length !== 8000) {
    throw new Error('Invalid secret key: expected 4000 bytes (8000 hex chars)')
  }

  const wasm = await getWasm()

  // Derive address and public key from secret key using WASM
  // The WASM module extracts the public key from the last 1952 bytes
  const infoJson = wasm.addressFromSecretKey(secretKeyHex)
  const info = JSON.parse(infoJson) as {
    address: string
    publicKey: string
    qtsAddress: string
  }

  // Check if wallet already exists
  const store = await loadStore()
  if (store.wallets.find((w) => w.address === info.address)) {
    throw new Error('Wallet already exists')
  }

  // Encrypt secret key with PIN
  const encryptedSecretKey = await encryptWithPin(secretKeyHex, pin)

  const wallet: StoredWallet = {
    address: info.address,
    qtsAddress: info.qtsAddress,
    rpcAddress: `QTS:${info.address}`,
    publicKey: info.publicKey,
    encryptedSecretKey,
    createdAt: Date.now(),
  }

  // Save to storage
  store.wallets.push(wallet)
  if (!store.activeAddress) {
    store.activeAddress = wallet.address
  }
  await saveStore(store)

  return wallet
}

/**
 * Unlock a wallet with PIN. Returns the unlocked wallet with secret key in memory.
 */
export async function unlockWallet(address: string, pin: string): Promise<UnlockedWallet> {
  const store = await loadStore()
  const wallet = store.wallets.find((w) => w.address === address)

  if (!wallet) {
    throw new Error('Wallet not found')
  }

  // Decrypt secret key with PIN (throws if wrong PIN)
  const secretKey = await decryptWithPin(wallet.encryptedSecretKey, pin)

  return {
    address: wallet.address,
    qtsAddress: wallet.qtsAddress,
    rpcAddress: `QTS:${wallet.address}`,
    publicKey: wallet.publicKey,
    secretKey,
  }
}

/**
 * Get all stored wallets (without secret keys).
 */
export async function getWallets(): Promise<StoredWallet[]> {
  const store = await loadStore()
  return store.wallets
}

/**
 * Get the active wallet address.
 */
export async function getActiveAddress(): Promise<string | null> {
  const store = await loadStore()
  return store.activeAddress
}

/**
 * Set the active wallet.
 */
export async function setActiveWallet(address: string): Promise<void> {
  const store = await loadStore()
  if (!store.wallets.find((w) => w.address === address)) {
    throw new Error('Wallet not found')
  }
  store.activeAddress = address
  await saveStore(store)
}

/**
 * Delete a wallet.
 */
export async function deleteWallet(address: string, pin: string): Promise<void> {
  // Verify PIN first
  await unlockWallet(address, pin)

  const store = await loadStore()
  store.wallets = store.wallets.filter((w) => w.address !== address)
  if (store.activeAddress === address) {
    store.activeAddress = store.wallets[0]?.address || null
  }
  await saveStore(store)
}

/**
 * Check if any wallet exists.
 */
export async function hasWallet(): Promise<boolean> {
  const store = await loadStore()
  return store.wallets.length > 0
}

// ── Transaction: Build + Sign + Broadcast ───────────────────

export interface SendParams {
  to: string            // hex address or qts1... address
  amount: string        // decimal string (in smallest unit, e.g. "1000000" = 1 QTEST)
  gasLimit?: number     // optional, defaults to 21000
  gasPrice?: number     // optional, defaults to 0 (gasless)
}

export interface SendResult {
  txHash: string        // hex transaction hash
  status: 'sent' | 'error'
  error?: string
}

/**
 * Build, sign, and broadcast a transfer transaction.
 * Uses WASM to build+sign (bincode), RPC to broadcast.
 */
export async function sendTransfer(
  wallet: UnlockedWallet,
  params: SendParams,
  rpcUrl?: string,
): Promise<SendResult> {
  try {
    if (rpcUrl) {
      rpc.setRpcUrl(rpcUrl)
    }

    // Get current nonce from node
    const nonce = await rpc.getNonce(wallet.rpcAddress)

    // Get chain info
    const chainId = await rpc.getChainIdNumber()
    const nodeInfo = await rpc.nodeInfo()
    const numShards = nodeInfo.num_shards

    // Resolve destination address
    let toHex = params.to
    if (params.to.startsWith('qts1')) {
      const wasm = await getWasm()
      toHex = wasm.qtsToAddress(params.to)
    }

    // Build and sign transaction with WASM (bincode serialization)
    const wasm = await getWasm()
    const signedJson = wasm.buildSignedTransfer(
      wallet.secretKey,
      toHex,
      params.amount,
      BigInt(nonce),
      BigInt(chainId),
      Number(numShards),
    )
    const signed = JSON.parse(signedJson) as {
      txHex: string
      txHash: string
    }

    // Broadcast via RPC
    const txHash = await rpc.sendRawTransaction(signed.txHex)

    return {
      txHash: rpc.parseQtsRaw(txHash),
      status: 'sent',
    }
  } catch (err) {
    return {
      txHash: '',
      status: 'error',
      error: err instanceof Error ? err.message : 'Unknown error',
    }
  }
}

